void qSortPrt(int *a, int n) {
    if (n < 2)
        return;
    int p = a[n / 2];
    int *l = a;
    int *r = a + n - 1;
    while (l <= r) {
		printf("n=%d, L = %d, R = %d, P = %d, \n", n, *l, *r, p);
        if (*l < p) {
            l++;
        }
        else if (*r > p) {
            r--;
        }
        else {
            int t = *l;
            *l = *r;
            *r = t;
            l++;
            r--;
        }
    }
	for (int i = 0; i < n; i++) { printf("[%d]", a[i]); }
	int nleft = r - a + 1;
	int nRight = a + n - l;
	printf("--nleft--%d--", nleft);
	
	printf("nRight--%d--", nRight);
	printf("\n");
	qSortPrt(a, nleft);
	qSortPrt(l, nRight);
}

